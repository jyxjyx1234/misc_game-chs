void PatchEXE(const wchar_t* wcPath)
{
	try
	{
		std::wstring exe_name = wcPath;

		RxStream::AutoMem exe_buf = exe_name;
		uint8_t* exe_ptr = exe_buf;
		uint32_t exe_size = exe_buf;
		uint32_t table_foa = 0;

		for (uint32_t ite_size = 0; ite_size < (exe_size - sizeof(sg_ucSJIS_Rang_Table)); ite_size++)
		{
			if (!memcmp(exe_ptr + ite_size, sg_ucSJIS_Rang_Table, sizeof(sg_ucSJIS_Rang_Table)))
			{
				table_foa = ite_size;
				break;
			}
		}

		if (table_foa == 0) { throw std::runtime_error("Not Find Table!"); }

		memcpy(exe_ptr + table_foa, sg_uiGBK_Rang_Table, sizeof(sg_uiGBK_Rang_Table));

		exe_buf.SaveDataToFile(exe_name + L".new");
		std::cout << "Replace Table!" << std::endl;
	}
	catch (const std::runtime_error& err)
	{
		std::cerr << err.what() << std::endl;
	}
}

int wmain(int argc, wchar_t** argv)
{
	if (argc > 1)
	{
		PatchEXE(argv[1]);
	}
	else
	{
		std::cout << "Rplace_RangTable.exe game.exe" << std::endl;
	}

	system("pause");
	return 0;
}